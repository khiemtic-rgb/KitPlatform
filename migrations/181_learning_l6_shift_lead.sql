Sau bài này bạn hướng tới vai trò **dẫn dắt vận hành ca** (không chỉ làm thao tác):
- Điều hành ca độc lập: phân công, theo dõi tiến độ, hỗ trợ NV.
- Xử lý tình huống; kiểm soát chất lượng dịch vụ trong ca.
- Theo dõi doanh thu / vận hành; báo cáo và bàn giao cuối ca.

## Vì sao quan trọng?

Ca trưởng không phải người làm nhiều nhất — mà giúp **cả đội làm hiệu quả**.
Chịu trách nhiệm đầu tiên về: chất lượng phục vụ · tiến độ · tinh thần đội · kết quả ca.

Ca trưởng **không làm thay** NV — giúp NV hoàn thành việc.

## Chương 1 — Vai trò

Điều phối nhân sự · theo dõi ca · hỗ trợ · kiểm tra quy trình · báo cáo quản lý.

## Chương 2 — Chuẩn bị trước ca

Lịch làm việc · nhân sự có mặt · phân công vị trí · Dashboard · nhiệm vụ · cảnh báo tồn / cận hạn · thiết bị.
Có vấn đề → giải quyết ngay hoặc báo QL.

## Chương 3 — Phân công

Biết ai phù hợp POS / tư vấn / CRM / kho · ai mới · ai xử lý khách khó.
Không phân công cảm tính — ưu tiên duy trì phục vụ khi thiếu người.

## Chương 4 — Theo dõi trong ca

Dashboard · doanh thu · lượng khách · quầy · checklist · nhiệm vụ · cảnh báo hệ thống.
Bất thường → can thiệp sớm.

## Chương 5 — Hướng dẫn & huấn luyện

Quan sát · góp ý · hướng dẫn · khích lệ · khen đúng lúc · sửa sai ngay.
Không: quát trước khách · chỉ trích tập thể · bỏ mặc NV mới.

## Chương 6 — Xử lý tình huống

Khiếu nại · thiếu thuốc · sai giá · POS/mạng lỗi · NV nghỉ đột xuất · đông khách.
Nguyên tắc: bình tĩnh → ưu tiên khách → phân công xử lý → ghi Novixa → báo QL nếu vượt quyền.

## Chương 7 — Kiểm soát chất lượng

Chào hỏi · tư vấn trong quyền · FEFO · CRM · checklist · không quá hạn trên kệ · khiếu nại đã xử lý.

## Chương 8 — Doanh thu (hiểu đúng)

Không chỉ «số đẹp»: số HĐ · AOV · khách quay lại · SP bán chạy · DT theo NV.
DT giảm → **phân tích** (khách, đơn, hàng, KM…) trước khi kết luận NV làm kém.

## Chương 9 — Báo cáo cuối ca

DT · tiền · checklist · nhiệm vụ · tồn bất thường · sự cố · bàn giao ca sau.

## Novixa hỗ trợ ca trưởng

Dashboard · KPI · DT · checklist · tiến độ NV · giao nhiệm vụ · sự cố · bàn giao · báo cáo cuối ca (theo quyền nhà thuốc bật).

## Lỗi thường gặp

Không phân công rõ · chỉ tự bán · bỏ Hub · bỏ checklist · không hỗ trợ NV · chậm khiếu nại · không ghi sự cố · không đánh giá sau ca.

## Checklist nhớ

**Trước:** nhân sự · phân công · Hub · thiết bị · checklist.
**Trong:** Hub · DT · checklist · hỗ trợ · sự cố.
**Cuối:** báo cáo · bàn giao · tiền · tồn · ghi nhận.

## Tình huống

1. NV nghỉ đột xuất → **điều chỉnh phân công**, giữ hoạt động, báo QL nếu cần.
2. Khách chờ lâu → **xin lỗi**, sắp xếp hỗ trợ ngay, theo dõi đến khi được phục vụ.
3. Checklist cuối còn thiếu → **hoàn thành trước khi kết ca** (không đăng xuất sớm).
4. DT giảm 30% → **phân tích** khách/đơn/hàng/KM trước khi kết luận.

## Thực hành (một nhiệm vụ lãnh đạo)

Điều hành / hỗ trợ điều hành ≥1 ca: phân công rõ · theo dõi Hub · can thiệp 1 tình huống · kiểm checklist · bàn giao / báo cáo cuối ca.
(Role-play với QL nếu đang tập — soft.)

## Quan sát bởi Quản lý (soft)

QL đánh giá: phân công · điều phối · hỗ trợ · xử lý tình huống · Hub · checklist · chất lượng · báo cáo cuối ca.
Đạt **≥ 80%** → «Đã áp dụng tại quầy».
Gợi ý thực tế: quan sát thêm trên vài ca trước khi giao vai ca trưởng cố định — soft, không khóa bán.

## Lộ trình tiếp (tương lai)

**L7 — Quản lý Nhà thuốc** · **L8 — Quản lý Chuỗi** — mở rộng Academy khi nhà thuốc sẵn sàng.

## Chúc mừng

Hoàn thành Foundation (L0–L6): sẵn sàng hướng tới vai trò **Ca trưởng & Quản lý quầy**.
