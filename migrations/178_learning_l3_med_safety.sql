Sau bài này bạn có thể:
- Hiểu quản lý hàng hóa là một phần **an toàn thuốc** (không chỉ «xếp kệ»).
- Nhập / kiểm tra lô & hạn đúng quy trình.
- Áp dụng FEFO; nhận biết cận hạn; báo hàng hỏng.
- Kiểm kê cơ bản; đảm bảo thuốc bán ra đạt chất lượng.

## Ba câu hỏi then chốt (An toàn thuốc)

1. **Điều gì gây rủi ro cho người bệnh?** Nhầm thuốc / nhầm lô · bán quá hạn · bảo quản sai · nhập sai số lượng.
2. **Novixa phòng ngừa thế nào?** Tồn kho · lô/HSD · cảnh báo cận hạn · gợi ý FEFO · lịch sử nhập/xuất · kiểm kê.
3. **Khi bất thường phải làm gì?** Cách ly · đánh dấu · **báo quản lý** — không tự ý xử lý / vẫn bán.

## Vì sao quan trọng?

Khách tin nhà thuốc vì tin thuốc được bảo quản và quản lý đúng.
Sai sót nhỏ (xuất nhầm lô, quá hạn, sai nhiệt độ, nhập sai SL) ảnh hưởng sức khỏe và uy tín.
Mỗi người có trách nhiệm **bảo vệ chất lượng trước khi thuốc đến tay khách**.

## Chương 1 — Quản lý hàng hóa gồm gì?

Nhập · kiểm tra · bảo quản · theo dõi tồn · kiểm kê · kiểm soát hạn · xuất bán đúng quy trình.

Luồng: Nhận hàng → Kiểm tra → Nhập kho → Sắp xếp → Bảo quản → Theo dõi tồn → Xuất bán → Kiểm kê.

## Chương 2 — Nhập hàng

Kiểm: đúng NCC · tên · hàm lượng · quy cách · số lượng · bao bì nguyên · có số lô · có HSD.
Bất thường → **không nhập** → báo quản lý.
Nếu được giao GRN trên Novixa: nhập đủ **SL + lô + HSD**, đối chiếu thực tế trước xác nhận.

## Chương 3 — Lô & hạn dùng

Mỗi SP cần số lô + HSD. Không nhập: không lô · không rõ hạn · hàng hư.
Ví dụ: Paracetamol · Lô PA250701 · HSD 07/2028.

## Chương 4 — FEFO

**First Expired First Out** — ưu tiên xuất lô hạn gần hơn (còn chất lượng).
Ví dụ Lô A HSD 05/2027 trước Lô B HSD 12/2027.
Không tự ý chọn lô mới hơn khi hệ thống đã gợi ý đúng.

## Chương 5 — Thuốc cận hạn

Còn hạn nhưng sắp hết theo quy định nhà thuốc.
Kiểm định kỳ · báo QL · dán nhãn nếu cần · không để quá hạn trên kệ.
Novixa hiển thị cảnh báo — **không bỏ qua**.

## Chương 6 — Bảo quản

Đúng nhiệt độ / độ ẩm · tránh nắng · không sát nền · không lẫn hàng lỗi.
Thuốc lạnh: đúng vị trí; không tự ý đổi chỗ.

## Chương 7 — Kiểm kê

Phát hiện thiếu / lệch / cận hạn / hàng hỏng.
Đếm → đối chiếu → xác nhận → báo cáo (theo quyền được giao).

## Chương 8 — Hàng lỗi / thuốc hỏng

Không bán: bao bì rách · móp nặng · đổi màu · chảy · quá hạn.
Phát hiện → tách riêng · đánh dấu · báo quản lý.

## Novixa hỗ trợ

Tồn kho · số lô · HSD · cảnh báo cận hạn · gợi ý FEFO · kiểm kê · lịch sử nhập/xuất.

## Lỗi thường gặp

Nhập sai SL/lô · không kiểm hạn · bỏ FEFO · để quá hạn trên kệ · không báo hàng hỏng · không kiểm kê.

## Checklist nhớ

**Nhập:** tên · SL · lô · hạn · bao bì.
**Bán:** FEFO · hạn · chất lượng.
**Định kỳ:** kiểm kê · cận hạn · báo hàng lỗi.

## Tình huống

1. Hai lô — HSD 05/2027 vs 12/2027 → bán **lô gần hạn trước**.
2. Hộp móp mạnh → **báo QL + cách ly**, không bán.
3. Novixa cảnh báo cận hạn → **kiểm tra và báo QL**.
4. SL thực tế ≠ hóa đơn → **kiểm lại, báo QL trước khi nhập**.

## Thực hành trên ca (một nhiệm vụ)

Tra cứu tồn / lô · kiểm FEFO khi bán ≥5 mặt · xem cảnh báo cận hạn (nếu có) · nếu được giao: hỗ trợ nhập/kiểm kê đúng quy trình · báo ngay khi thấy hàng lỗi.

## Quan sát tại quầy (soft)

QL đánh giá: kiểm lô/hạn · FEFO · nhập đúng (nếu giao) · báo hàng lỗi · kiểm kê đúng · tuân thủ quy trình.
Đạt **≥ 80%** → «Đã áp dụng tại quầy». Không khóa bán · không chặn L4.

## Sau bài này

Đọc → quiz ≥80% → áp dụng trên ca → QL quan sát (soft) → **L4 — Vận hành ca làm việc**.
