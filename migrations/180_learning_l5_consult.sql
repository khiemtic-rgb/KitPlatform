Sau bài này bạn hướng tới vai trò **tư vấn tại quầy** (trong quyền), không chỉ «bán hàng»:
- Giao tiếp chuyên nghiệp; xác định đúng nhu cầu.
- Đề xuất & hướng dẫn dùng an toàn; bổ trợ chỉ khi thật cần.
- Xây niềm tin → khách trung thành → doanh thu tăng bền vững.

## Triết lý L5

**Bán đúng → Tư vấn đúng → Chăm sóc đúng → Khách quay lại → Doanh thu bền vững.**

Khách không chỉ quay lại vì rẻ nhất — mà vì được lắng nghe, tư vấn đúng, hướng dẫn rõ, tin người tư vấn.
Một lần bán = DT hôm nay. Một lần tư vấn tốt = khách nhiều năm.

## Chương 1 — Tư vấn ≠ bán hàng

Bán hàng: giới thiệu sản phẩm.
Tư vấn: giúp chọn **giải pháp phù hợp**.
Không cố bán nhiều nhất — mục tiêu: đúng nhu cầu · đúng SP · đúng cách dùng · đúng kỳ vọng.
Tin tưởng → DT tăng tự nhiên.

## Chương 2 — Quy trình tư vấn

Chào → Lắng nghe → Đặt câu hỏi → Xác định nhu cầu → Đề xuất → Giải thích → Hướng dẫn dùng → Xác nhận đã hiểu → Thanh toán → Chăm sóc sau bán.
Không bỏ bước.

Vượt quyền / kê đơn / kháng sinh không đơn / liều phức tạp → **mời dược sĩ phụ trách**, không đoán.

## Chương 3 — Lắng nghe

Ai dùng? Triệu chứng? Đã dùng gì? Dị ứng? Đang điều trị bệnh khác?
Lắng nghe nhiều hơn nói — chưa hiểu nhu cầu thì chưa giới thiệu SP.

## Chương 4 — Đề xuất phù hợp

Chọn đúng · giải thích lý do · cách dùng · thời gian · lưu ý / tác dụng phụ nếu biết trong quyền.
Không: ép mua · SP không phù hợp · cam kết quá mức · bán theo quảng cáo khi triệu chứng không khớp.

## Chương 5 — Hướng dẫn sử dụng

Liều · thời điểm · bảo quản · lưu ý · khi nào nên đi khám.
Đây là trách nhiệm nhà thuốc — không chỉ đưa hộp thuốc.

## Chương 6 — Sản phẩm bổ trợ

Vitamin · men vi sinh · TBYT · chăm sóc sức khỏe — **chỉ khi thật sự có lợi**.
Không bán bằng mọi giá · không tạo cảm giác bị ép.

## Chương 7 — Chăm sóc sau bán (nối L2)

CRM · điểm · nhắc thuốc · hỏi thăm theo kênh cho phép · mời quay lại khi cần.
Khách quay lại nhiều lần > khách mua một lần.

## Novixa hỗ trợ

Lịch sử mua · gợi ý (khi có) · khách thân thiết · nhắc thuốc · tin nhắn CSKH · theo dõi DT cá nhân / quay lại (khi nhà thuốc bật).

## Lỗi thường gặp

Chỉ nói giá · không hỏi nhu cầu · không hướng dẫn dùng · ép mua thêm · bỏ CRM · không nhắc tái mua · không cảm ơn · tư vấn vượt quyền.

## Checklist nhớ

**Trước:** chào · lắng nghe · hỏi · hiểu nhu cầu.
**Trong:** đề xuất đúng · giải thích · hướng dẫn dùng · giải đáp.
**Sau:** CRM · nhắc thuốc nếu cần · cảm ơn.

## Tình huống

1. Khách đòi thuốc theo quảng cáo nhưng triệu chứng không khớp → **hỏi thêm và tư vấn phù hợp** (không bán ngay).
2. Xin kháng sinh không đơn → **giải thích quy định**, hướng dẫn khám khi cần — không bán để giữ khách.
3. Từ chối SP bổ trợ → **tôn trọng**, hoàn tất tư vấn.
4. Quay lại phản ánh không hiệu quả → **lắng nghe**, tìm hiểu, báo DS/QL nếu vượt quyền — không tranh luận.

## Thực hành trên ca (một nhiệm vụ)

Trong ca: ≥3 lần tư vấn đủ bước (nghe → đề xuất → hướng dẫn dùng) · cập nhật CRM · đặt nhắc nếu có liệu trình · không ép bổ trợ · mời DS khi vượt quyền.

## Quan sát tại quầy (soft)

QL đánh giá một ca tư vấn: chào · lắng nghe · hỏi · tư vấn đúng nhu cầu · hướng dẫn dùng · không ép bán · CRM · chăm sóc sau bán.
Đạt **≥ 80%** → «Đã áp dụng tại quầy». Không khóa bán.

## Gợi ý tương lai — Consultation Score (chưa bật)

Đánh giá chất lượng tư vấn (không chỉ doanh số): tuân thủ quy trình · CRM/nhắc thuốc · khách quay lại · bổ trợ phù hợp (không ép) → khuyến khích tư vấn đúng.

## Sau bài này

Đọc → quiz ≥80% → thực hành tư vấn trên ca → QL quan sát (soft) → hướng tới **L6 — Ca trưởng & Quản lý quầy** (khi lộ trình mở).
